const seedCustomers = async () => {
  try {
    await connectDB();
    console.log('✅ Connected to database');

    // Get the first organization
    const organization = await Organization.findOne();
    if (!organization) {
      console.error('❌ No organization found. Please create an organization first.');
      process.exit(1);
    }

    console.log(`📋 Using organization: ${organization.name}`);

    // Delete existing customers for this organization
    await Customer.deleteMany({ organizationId: organization._id });
    console.log('🗑️  Cleared existing customers');

    // Add organizationId to all sample customers
    const customersWithOrg = sampleCustomers.map(customer => ({
      ...customer,
      organizationId: organization._id,
    }));

    // Insert sample customers
    const customers = await Customer.insertMany(customersWithOrg);
    console.log(`✅ Successfully seeded ${customers.length} customers:`);
    
    customers.forEach((customer, index) => {
      console.log(`   ${index + 1}. ${customer.customerCode} - ${customer.companyName}`);
    });

    console.log('\n📊 Summary:');
    console.log(`   Total Customers: ${customers.length}`);
    console.log(`   Active: ${customers.filter(c => c.status === 'Active').length}`);
    console.log(`   Dealers: ${customers.filter(c => c.customerType === 'Dealer').length}`);
    console.log(`   Distributors: ${customers.filter(c => c.customerType === 'Distributor').length}`);
    console.log(`   Retailers: ${customers.filter(c => c.customerType === 'Retailer').length}`);

    process.exit(0);
  } catch (error) {
    console.error('❌ Error seeding customers:', error);
    process.exit(1);
  }
};

// Run the seed function
seedCustomers();
