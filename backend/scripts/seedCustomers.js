async function seedCustomers() {
  try {
    await mongoose.connect('mongodb://localhost:27017/erp_system');
    console.log('Connected to MongoDB');

    // Clear existing customers for this org
    await Customer.deleteMany({ organizationId: ORG_ID });
    console.log('Cleared existing customers');

    // Insert new customers
    const result = await Customer.insertMany(customers);
    console.log(`✅ Successfully added ${result.length} customers`);

    // Display added customers
    result.forEach((customer, index) => {
      console.log(`${index + 1}. ${customer.companyName || `${customer.firstName} ${customer.lastName}`} - ${customer.customerCode}`);
    });

    process.exit(0);
  } catch (error) {
    console.error('Error seeding customers:', error);
    process.exit(1);
  }
}

