/**
 * @desc    Create new organization
 * @route   POST /api/admin/organizations
 * @access  Private/SuperAdmin
 */
exports.createOrganization = async (req, res) => {
  try {
    const {
      name,
      slug,
      email,
      phone,
      address,
      subscriptionPlan,
      enabledFeatures,
      adminUser,
      settings,
      limits,
      seedData = true, // Flag to seed initial data
      copyFromOrg, // Optional: ID of organization to copy data from
    } = req.body;

    // Check if organization with same email or slug exists
    const existingOrg = await Organization.findOne({
      $or: [{ email }, { slug }]
    });

    if (existingOrg) {
      return res.status(400).json({
        success: false,
        message: 'Organization with this email or slug already exists',
      });
    }

    // Create organization (database name will be auto-generated by pre-save hook)
    const organization = await Organization.create({
      name,
      slug,
      email,
      phone,
      address,
      subscriptionPlan: subscriptionPlan || 'TRIAL',
      subscriptionStatus: 'ACTIVE',
      subscriptionStartDate: Date.now(),
      subscriptionEndDate: new Date(Date.now() + 30 * 24 * 60 * 60 * 1000), // 30 days trial
      enabledFeatures: enabledFeatures || [],
      adminUser: {
        firstName: adminUser.firstName,
        lastName: adminUser.lastName,
        email: adminUser.email,
        phone: adminUser.phone,
        password: adminUser.password,
        isActive: true,
      },
      settings: settings || {},
      limits: limits || {},
      isActive: true,
      isVerified: true,
      verifiedAt: Date.now(),
      createdBy: req.user?._id, // Optional - handle cases where req.user might not exist
    });

    const dbName = organization.database.name;
    logger.info(`✓ Organization created: ${organization.name} (ID: ${organization._id})`);
    logger.info(`  Database name: ${dbName}`);
    logger.info(`  Created by: ${req.user.email}`);

    // Initialize tenant database and models
    let dbInitialized = false;
    let seedingResult = null;

    try {
      logger.info(`Initializing tenant database models for: ${dbName}`);
      await initializeTenantModels(dbName, organization._id);
      dbInitialized = true;
      logger.info(`✓ Tenant database models initialized`);

      // Seed initial data if requested
      if (seedData) {
        const seedOptions = {};

        // If copyFromOrg is provided, copy data from that organization
        if (copyFromOrg) {
          seedOptions.copyData = true;
          seedOptions.templateOrgId = copyFromOrg;
          logger.info(`Copying data from organization: ${copyFromOrg}`);
        } else {
          logger.info(`Seeding default data for new organization`);
        }

        seedingResult = await seedTenantData(dbName, organization._id, seedOptions);
        logger.info(`✓ Initial data seeded successfully for ${organization.name}`);
        logger.info(`  Data created: ${JSON.stringify(seedingResult.data)}`);
      } else {
        logger.warn(`⚠ Seeding skipped (seedData=false). Database ${dbName} will be empty!`);
      }
    } catch (dbError) {
      logger.error(`❌ Error initializing tenant database for ${dbName}: ${dbError.message}`);
      logger.error(`Stack trace: ${dbError.stack}`);

      // Return error response since database creation failed
      return res.status(500).json({
        success: false,
        message: 'Organization created but database initialization failed',
        error: dbError.message,
        organization: {
          id: organization._id,
          name: organization.name,
          dbName: dbName,
        },
      });
    }

    res.status(201).json({
      success: true,
      message: 'Organization created successfully with isolated database',
      data: {
        ...organization.toObject(),
        database: {
          name: dbName,
          initialized: dbInitialized,
          seeded: seedData && seedingResult !== null,
          seedingData: seedingResult?.data || null,
        },
      },
    });

  } catch (error) {
    logger.error(`Create organization error: ${error.message}`);
    res.status(500).json({
      success: false,
      message: 'Error creating organization',
      error: error.message,
    });
  }
};

/**
 * @desc    Get all organizations
 * @route   GET /api/admin/organizations
 * @access  Private/SuperAdmin
 */
exports.getAllOrganizations = async (req, res) => {
  try {
    const { page = 1, limit = 10, status, search, includeInactive } = req.query;

    const query = {};

    // By default, only show active organizations unless includeInactive is set
    if (includeInactive !== 'true') {
      query.isActive = true;
    }

    if (status) {
      query.subscriptionStatus = status;
    }

    if (search) {
      query.$or = [
        { name: { $regex: search, $options: 'i' } },
        { email: { $regex: search, $options: 'i' } },
        { slug: { $regex: search, $options: 'i' } },
      ];
    }

    const organizations = await Organization.find(query)
      .populate('enabledFeatures.featureId')
      .sort({ createdAt: -1 })
      .limit(limit * 1)
      .skip((page - 1) * limit)
      .select('-adminUser.password');

    const count = await Organization.countDocuments(query);

    res.status(200).json({
      success: true,
      data: organizations,
      pagination: {
        total: count,
        page: parseInt(page),
        pages: Math.ceil(count / limit),
      },
    });

  } catch (error) {
    logger.error(`Get organizations error: ${error.message}`);
    res.status(500).json({
      success: false,
      message: 'Error fetching organizations',
      error: error.message,
    });
  }
};

