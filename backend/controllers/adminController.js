/**
 * @desc    Update organization
 * @route   PUT /api/admin/organizations/:id
 * @access  Private/SuperAdmin
 */
exports.updateOrganization = async (req, res) => {
  try {
    let organization = await Organization.findById(req.params.id).select('+adminUser.password');

    if (!organization) {
      return res.status(404).json({
        success: false,
        message: 'Organization not found',
      });
    }

    // Update fields
    const updateFields = [
      'name', 'phone', 'address', 'settings', 'limits',
      'subscriptionPlan', 'subscriptionStatus', 'subscriptionEndDate',
      'isActive', 'branding', 'notes'
    ];

    updateFields.forEach(field => {
      if (req.body[field] !== undefined) {
        organization[field] = req.body[field];
      }
    });

    // Handle Admin User updates
    if (req.body.adminUser) {
      if (req.body.adminUser.firstName) organization.adminUser.firstName = req.body.adminUser.firstName;
      if (req.body.adminUser.lastName) organization.adminUser.lastName = req.body.adminUser.lastName;
      if (req.body.adminUser.email) organization.adminUser.email = req.body.adminUser.email;
      if (req.body.adminUser.phone) organization.adminUser.phone = req.body.adminUser.phone;
      if (req.body.adminUser.password) {
        console.log('🔑 [Update] New password provided for:', organization.adminUser.email);
        organization.adminUser.password = req.body.adminUser.password;
        organization.markModified('adminUser.password'); // Force Mongoose to notice the change
      }
    }

    console.log('📝 [Update] Organization modification status:', organization.isModified('adminUser.password'));

    if (req.user && req.user._id && mongoose.Types.ObjectId.isValid(req.user._id)) {
      organization.lastModifiedBy = req.user._id;
    }
    await organization.save();

    logger.info(`Organization updated: ${organization.name} by ${req.user.email}`);

    res.status(200).json({
      success: true,
      message: 'Organization updated successfully',
      data: organization,
    });

  } catch (error) {
    console.error('❌ [Update Organization Error]:', error);
    logger.error(`Update organization error: ${error.message}`);
    res.status(500).json({
      success: false,
      message: 'Error updating organization: ' + error.message,
      error: error.stack,
    });
  }
};

/**
 * @desc    Update organization features (Enable/Disable modules)
 * @route   PUT /api/admin/organizations/:id/features
 * @access  Private/SuperAdmin
 */
exports.updateOrganizationFeatures = async (req, res) => {
  try {
    const { enabledFeatures } = req.body;

    if (!enabledFeatures || !Array.isArray(enabledFeatures)) {
      return res.status(400).json({
        success: false,
        message: 'enabledFeatures must be an array',
      });
    }

    let organization = await Organization.findById(req.params.id);

    if (!organization) {
      return res.status(404).json({
        success: false,
        message: 'Organization not found',
      });
    }

    // Validate feature IDs if there are any features to enable
    if (enabledFeatures.length > 0) {
      const featureIds = enabledFeatures.map(f => f.featureId);
      const features = await Feature.find({ _id: { $in: featureIds } });

      if (features.length !== featureIds.length) {
        logger.error(`Feature validation failed. Provided: ${featureIds.length}, Found: ${features.length}`);
        return res.status(400).json({
          success: false,
          message: 'One or more invalid feature IDs',
          details: {
            provided: featureIds.length,
            valid: features.length,
          }
        });
      }
    }

    organization.enabledFeatures = enabledFeatures;
    organization.lastModifiedBy = req.user._id;
    await organization.save();

    logger.info(`Features updated for organization: ${organization.name} (${enabledFeatures.length} features) by ${req.user.email}`);

    res.status(200).json({
      success: true,
      message: 'Features updated successfully',
      data: organization,
    });

  } catch (error) {
    logger.error(`Update features error: ${error.message}`);
    logger.error(`Stack: ${error.stack}`);
    res.status(500).json({
      success: false,
      message: 'Error updating features',
      error: error.message,
    });
  }
};

