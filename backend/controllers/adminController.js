/**
 * @desc    Add secondary admin to organization
 * @route   POST /api/admin/organizations/:id/admins
 * @access  Private/SuperAdmin
 */
exports.addSecondaryAdmin = async (req, res) => {
  try {
    const { firstName, lastName, email, phone, password } = req.body;

    if (!firstName || !lastName || !email || !password) {
      return res.status(400).json({ success: false, message: 'Missing required fields' });
    }

    const organization = await Organization.findById(req.params.id);
    if (!organization) {
      return res.status(404).json({ success: false, message: 'Organization not found' });
    }

    // Check if email already exists in primary or secondary admins
    const isPrimary = organization.adminUser.email === email.toLowerCase();
    const isSecondary = organization.secondaryAdmins.some(admin => admin.email === email.toLowerCase());

    if (isPrimary || isSecondary) {
      return res.status(400).json({ success: false, message: 'Admin with this email already exists' });
    }

    organization.secondaryAdmins.push({
      firstName,
      lastName,
      email: email.toLowerCase(),
      phone,
      password,
      isActive: true
    });

    await organization.save();

    res.status(200).json({
      success: true,
      message: 'Secondary admin added successfully',
      data: organization.secondaryAdmins
    });
  } catch (error) {
    logger.error('Add secondary admin error:', error);
    res.status(500).json({ success: false, message: error.message });
  }
};

/**
 * @desc    Update secondary admin details
 * @route   PUT /api/admin/organizations/:id/admins/:adminId
 * @access  Private/SuperAdmin
 */
exports.updateSecondaryAdmin = async (req, res) => {
  try {
    const { firstName, lastName, email, phone, password } = req.body;
    const organization = await Organization.findById(req.params.id);

    if (!organization) {
      return res.status(404).json({ success: false, message: 'Organization not found' });
    }

    const adminIndex = organization.secondaryAdmins.findIndex(
      admin => admin._id.toString() === req.params.adminId
    );

    if (adminIndex === -1) {
      return res.status(404).json({ success: false, message: 'Admin not found' });
    }

    // Update fields
    if (firstName) organization.secondaryAdmins[adminIndex].firstName = firstName;
    if (lastName) organization.secondaryAdmins[adminIndex].lastName = lastName;
    if (email) organization.secondaryAdmins[adminIndex].email = email.toLowerCase();
    if (phone) organization.secondaryAdmins[adminIndex].phone = phone;

    if (password) {
      organization.secondaryAdmins[adminIndex].password = password;
      organization.markModified(`secondaryAdmins.${adminIndex}.password`);
    }

    await organization.save();

    res.status(200).json({
      success: true,
      message: 'Secondary admin updated successfully',
      data: organization.secondaryAdmins
    });
  } catch (error) {
    logger.error('Update secondary admin error:', error);
    res.status(500).json({ success: false, message: error.message });
  }
};

