/**
 * @desc    Get all payment history across all organizations
 * @route   GET /api/admin/payments
 * @access  Private/SuperAdmin
 */
exports.getAllPaymentHistory = async (req, res) => {
  try {
    const Subscription = require('../models/shared/Subscription');

    const payments = await Subscription.aggregate([
      { $unwind: '$paymentHistory' },
      {
        $lookup: {
          from: 'organizations',
          localField: 'organizationId',
          foreignField: '_id',
          as: 'organization'
        }
      },
      { $unwind: '$organization' },
      {
        $project: {
          _id: '$paymentHistory._id',
          organizationId: 1,
          organizationName: '$organization.name',
          amount: '$paymentHistory.amount',
          plan: '$paymentHistory.plan',
          type: '$paymentHistory.type',
          paymentDate: '$paymentHistory.paymentDate',
          transactionId: '$paymentHistory.transactionId',
          paymentMethod: '$paymentHistory.paymentMethod',
          paymentStatus: '$paymentHistory.paymentStatus',
          description: '$paymentHistory.description'
        }
      },
      { $sort: { paymentDate: -1 } }
    ]);

    res.status(200).json({
      success: true,
      data: payments
    });
  } catch (error) {
    logger.error('Get all payments error:', error);
    res.status(500).json({ success: false, message: error.message });
  }
};

/**
 * @desc    Get all organizations with detailed subscription info
 * @route   GET /api/admin/subscriptions
 * @access  Private/SuperAdmin
 */
exports.getAllSubscriptionDetails = async (req, res) => {
  try {
    const Organization = require('../models/shared/Organization');

    const subscriptions = await Organization.aggregate([
      {
        $lookup: {
          from: 'subscriptions',
          localField: '_id',
          foreignField: 'organizationId',
          as: 'subscription'
        }
      },
      {
        $unwind: {
          path: '$subscription',
          preserveNullAndEmptyArrays: true
        }
      },
      {
        $project: {
          _id: 1,
          name: 1,
          email: 1,
          phone: 1, // Added phone for contact
          logo: '$branding.logo',
          subscription: {
            status: { $ifNull: ['$subscription.status', 'inactive'] },
            plan: { $ifNull: ['$subscription.plan', 'Free'] },
            planPrice: '$subscription.planPrice',
            startDate: '$subscription.startDate',
            endDate: '$subscription.endDate',
            autoRenew: '$subscription.autoRenew',
            // Token Details
            freeTokens: '$subscription.freeTokens',
            purchasedTokens: '$subscription.purchasedTokens',
            // Payment History for this org
            paymentHistory: '$subscription.paymentHistory'
          }
        }
      },
      { $sort: { 'subscription.endDate': 1 } } // Sort by expiry date (soonest first)
    ]);

    res.status(200).json({
      success: true,
      data: subscriptions
    });
  } catch (error) {
    logger.error('Get subscription details error:', error);
    res.status(500).json({ success: false, message: error.message });
  }
};

/**
 * @desc    Get system statistics
 * @route   GET /api/admin/stats
 * @access  Private/SuperAdmin
 */
exports.getSystemStats = async (req, res) => {
  try {
    const totalOrganizations = await Organization.countDocuments();
    const activeOrganizations = await Organization.countDocuments({
      isActive: true,
      subscriptionStatus: 'ACTIVE'
    });
    const trialOrganizations = await Organization.countDocuments({
      subscriptionPlan: 'TRIAL'
    });

    const stats = {
      totalOrganizations,
      activeOrganizations,
      inactiveOrganizations: totalOrganizations - activeOrganizations,
      trialOrganizations,
      organizationsByPlan: await Organization.aggregate([
        { $group: { _id: '$subscriptionPlan', count: { $sum: 1 } } }
      ]),
      organizationsByStatus: await Organization.aggregate([
        { $group: { _id: '$subscriptionStatus', count: { $sum: 1 } } }
      ]),
    };

    res.status(200).json({
      success: true,
      data: stats,
    });

  } catch (error) {
    logger.error(`Get stats error: ${error.message}`);
    res.status(500).json({
      success: false,
      message: 'Error fetching statistics',
      error: error.message,
    });
  }
};
